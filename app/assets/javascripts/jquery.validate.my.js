jQuery.validator.addMethod(
  "username",
  function(value, element) {
    return /^[a-zA-Z][a-zA-Z0-9_]*$/.test(value) || value.length == 0
  }, "用户名只支持字母、数字、下划线组合，并且以字母开头"
)


