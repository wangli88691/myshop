jQuery.validator.addMethod(
  "date_gte",
  function(value, element, param) {
    var target = $(param);
    return value >= target.val();
  }, "结束日期不能小于开始日期"
)

jQuery.validator.addMethod(
  "image",
  function( value, element ) {
    return this.optional(element) || /\.(jpg|jpeg|gif|png)$/i.test(value);
  }, jQuery.format("请上传图片(png, jpg, jpeg, gif)")
)

